# ── MIME helpers (used when mail seeder is fully wired) ───────────────────────

def _build_mime(art: MailArtefact) -> MIMEMultipart:
    msg = MIMEMultipart("alternative")
    msg["Subject"] = art.subject
    msg["From"] = f"noreply@{art.to_email.split('@')[1]}"
    msg["To"] = art.to_email
    msg.attach(MIMEText(art.body_text, "plain"))
    msg.attach(MIMEText(art.body_html, "html"))
    return msg
