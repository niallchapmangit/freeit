# ── Files seeder (Nextcloud WebDAV) ──────────────────────────────────────────

class FilesSeeder:
    """
    Uploads Markdown files to Nextcloud via WebDAV.
    Idempotent: checks if the file already exists (HEAD) before uploading.
    Uses per-user app passwords generated by bootstrap-realm.sh (E1.3).
    """

    def __init__(self, base_url: str, app_password_fn: "AppPasswordFn") -> None:
        """
        base_url:        e.g. https://files.acme-demo.yourdemo.com
        app_password_fn: callable(email) → Nextcloud app password for that user
        """
        self._base = base_url.rstrip("/")
        self._app_password = app_password_fn

    def seed(self, artefacts: list[FileArtefact]) -> dict[str, str]:
        results: dict[str, str] = {}
        for art in artefacts:
            outcome = self._upload(art)
            results[art.webdav_path] = outcome
        return results

    def _upload(self, art: FileArtefact) -> str:
        password = self._app_password(art.owner_email)
        webdav_url = f"{self._base}/remote.php/dav/files/{art.owner_email}{art.webdav_path}"
        auth = (art.owner_email, password)

        # Idempotency: skip if file already exists.
        head = requests.head(webdav_url, auth=auth, timeout=15)
        if head.status_code == 200:
            return "skipped"

        # Ensure parent directory exists.
        parent = art.webdav_path.rsplit("/", 1)[0]
        if parent:
            self._mkdirs(art.owner_email, password, parent)

        resp = requests.put(
            webdav_url,
            data=art.content.encode("utf-8"),
            auth=auth,
            headers={"Content-Type": "text/markdown; charset=utf-8"},
            timeout=30,
        )
        resp.raise_for_status()
        return "created"

    def _mkdirs(self, username: str, password: str, path: str) -> None:
        """MKCOL all path segments that don't yet exist."""
        auth = (username, password)
        parts = path.strip("/").split("/")
        for i in range(1, len(parts) + 1):
            segment = "/".join(parts[:i])
            url = f"{self._base}/remote.php/dav/files/{username}/{segment}"
            head = requests.head(url, auth=auth, timeout=10)
            if head.status_code == 404:
                requests.request("MKCOL", url, auth=auth, timeout=10)


